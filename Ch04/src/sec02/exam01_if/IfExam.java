package sec02.exam01_if;

public class IfExam {
    public static void main(String args[]) {
        int score = 83;

        if(score >= 90) {
            System.out.println("점수가 90점보다 큽니다.");
            System.out.println("등급 A입니다.");
        } else {
            System.out.println("점수가 90점보다 작습니다.");
            System.out.println("등급 B입니다.");
        }
    }
}
