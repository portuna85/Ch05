package sec03.exam01_sign;

public class SignOperatorExample {
    public static void main(String args[]) {
        int i1 = +100;
        int i2 = -100;
        double d1 = +3.14;
        double d2 = -3.14;

        System.out.println(i1);
        System.out.println(-i2);
        System.out.println(d1);
        System.out.println(d2);
    }
}
